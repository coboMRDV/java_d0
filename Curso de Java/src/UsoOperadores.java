
public class UsoOperadores {

	public static void main(String[] args) {
		int num1 = 7;
		int num2 = 5;
		int resultado;
		
		resultado = num1 + num2;
		resultado = num1 - num2;
		resultado = num1 / num2;
		resultado = num1 * num2;
		resultado ++;
		resultado --;
		resultado += 10;
		resultado -= 2;
		
		System.out.println("Resultado: " + resultado);

	}

}
